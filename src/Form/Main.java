    private void bHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHitungActionPerformed
        DefaultTableModel model = (DefaultTableModel) tableKeranjang.getModel();
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Keranjang Kosong!");
            nonAktifPembayaran();
            bersihPembayaran();
            return;
        }else{
            aktifPembayaran();
            double totalHarga = 0;
            int totalItem = 0;

            for (int i = 0; i < model.getRowCount(); i++) {
                int jumlah = Integer.parseInt(model.getValueAt(i, 3).toString()); 
                double subtotal = Double.parseDouble(model.getValueAt(i, 4).toString()); 
                totalItem += jumlah;
                totalHarga += subtotal;
            }

            bayar.setSubTotal(totalHarga);
            bayar.hitungDiskon(totalItem);
            bayar.hitungPajak();
            bayar.hitungTotalBayar();

            // tampil di field
            tSubtotal.setText(Double.toString(bayar.getSubTotal()));
            tDiskon.setText(Double.toString(bayar.getDiskon()));
            tPajak.setText(Double.toString(bayar.getPajak()));
            tTotalBayar.setText(Double.toString(bayar.getTotalBayar()));
            tNamaPembeli.requestFocus();
        }
    }//GEN-LAST:event_bHitungActionPerformed

    private void tableKeranjangPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_tableKeranjangPropertyChange
        // TODO add your handling code here:
        int row = tableKeranjang.getSelectedRow();

        if (row != -1) {
            try {
                double harga = Double.parseDouble(tableKeranjang.getValueAt(row, 2).toString());
                int jumlah = Integer.parseInt(tableKeranjang.getValueAt(row, 3).toString());
                if (jumlah > 0) {
                    double total = jumlah * harga;

                    tableKeranjang.setValueAt(total, row, 4);
                }else{
                    JOptionPane.showMessageDialog(this, "Input tidak valid!");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Input tidak valid!");
            }

        }
    }//GEN-LAST:event_tableKeranjangPropertyChange

    private void BHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHapusActionPerformed
        // TODO add your handling code here:
        int row = tableKeranjang.getSelectedRow();

        if (row != -1) {
            DefaultTableModel model = (DefaultTableModel) tableKeranjang.getModel();
            model.removeRow(row);
            if (tableKeranjang.getRowCount() == 0) {
                nonAktifPembayaran();
                bersihPembayaran();
                return;
            }else{
                aktifPembayaran();
            }

        }else{
            JOptionPane.showMessageDialog(this, "Pilih item yang ingin dihapus!");
        }
    }//GEN-LAST:event_BHapusActionPerformed


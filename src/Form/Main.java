    private void tUangBayarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tUangBayarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                bayar.setUangBayar(Double.parseDouble(tUangBayar.getText()));
                tUangKembali.setText(Double.toString(bayar.getUangKembali()));
                if (bayar.getUangBayar() < bayar.getTotalBayar()) {
                    JOptionPane.showMessageDialog(null, "Pembayaran Kurang!",
                        "Warning", JOptionPane.OK_OPTION);
                    tUangBayar.setText("");
                    tUangKembali.setText("");
                    tUangKembali.setEnabled(false);
                }else{
                    tUangKembali.setEnabled(true);
                    bCetak.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Input tidak valid!");
                return;
            }
        }else{
            return;
        }
    }//GEN-LAST:event_tUangBayarKeyPressed


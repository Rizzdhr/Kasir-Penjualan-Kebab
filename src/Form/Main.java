    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (cmbMenu.getSelectedItem().equals("PILIH") ||
            bgUkuran.getSelection() == null ||
            tHarga.getText().isEmpty() ||
            ((int) spJumlahBeli.getValue() <= 0 ||
                tTotal.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Semua kolom pesanan harus diisi", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;

        }else{
            DefaultTableModel model = (DefaultTableModel) tableKeranjang.getModel();
            
            String menu = kebab.getNama();
            String ukuran = kebab.getUkuran();
            
            boolean isSame = false;
            for (int i = 0; i < model.getRowCount(); i++){
                String menuTabel = model.getValueAt(i, 0).toString();
                String ukuranTabel = model.getValueAt(i, 1).toString();
                
                if (menu.equalsIgnoreCase(menuTabel) && ukuran.equalsIgnoreCase(ukuranTabel)) {
                    int jumlahLama = Integer.parseInt(model.getValueAt(i, 3).toString());
                    int jumlahGabung = jumlahLama + kebab.getJumlahBeli();
                    double totalGabung = jumlahGabung * kebab.getHarga();
                    
                    model.setValueAt(jumlahGabung, i, 3);
                    model.setValueAt(totalGabung, i, 4);
                    isSame = true;
                    break;
                }
            }
            
            if (!isSame) {
                model.addRow(new Object[]{
                    kebab.getNama(),
                    kebab.getUkuran(),
                    kebab.getHarga(),
                    kebab.getJumlahBeli(),
                    kebab.getTotal(),    
                });
            }

            bersih();
            cmbMenu.setSelectedItem("PILIH");
        }
    }//GEN-LAST:event_btnAddActionPerformed

